const num1 = Number(prompt("Digite um número:"));
const num2 = Number(prompt("Digite outro número:"));

const soma = num1 + num2;
const sub = num1 - num2;
const multi = num1 * num2;
const div = num1 / num2;

console.log(`Soma:\t ${num1} + ${num2} = ${soma}`);
console.log(`Subtração:\t ${num1} - ${num2} = ${sub}`);
console.log(`Multiplicação:\t ${num1} * ${num2} = ${multi}`);
console.log(`Divisão:\t ${num1} / ${num2} = ${div}`);